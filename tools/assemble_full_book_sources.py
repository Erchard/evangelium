def localize_paper_statuses_uk(line: str) -> str:
    line = line.replace("Reader status:", "Статус у читацькому виданні:")
    line = line.replace("Greek witness status:", "Грецьке свідчення:")
    line = line.replace("Publication status:", "Статус публікації:")
    for code, label in PAPER_STATUS_TERMS_UK.items():
        line = re.sub(rf"`?{re.escape(code)}`?", label, line)
    return line


def sanitize_paper_text(text: str, lang: str = "en") -> str:
    text = re.sub(r"\*\*([^*\n]+)\*\*", r"\1", text)
    text = re.sub(r";?\s*локальний робочий текст:\s*`[^`]+`", "", text)
    text = re.sub(r";?\s*[ув] проекті використано локальний робочий текст\s*`[^`]+`", "", text)
    text = re.sub(r";?\s*[ув] проекті використано локальний робочий текст\s*(?:`[^`]+`)?\s*\.{0,2}", "", text)
    text = re.sub(r";?\s*локальний робочий текст\s*(?:`[^`]+`)?\s*\.{0,2}", "", text)
    text = re.sub(r"`[^`]*(?:corpus|reconstruction|project|sources|controls|notes|output)/[^`]*`", "", text)
    text = text.replace("`", "")
    if lang == "uk":
        text = normalize_uk_reader_caution_language(text)
    text = re.sub(r"\s+\n", "\n", text)
    lines = []
    for line in text.splitlines():
        stripped = line.strip()
        if re.search(r"(?:corpus|reconstruction|project|sources|controls|notes|output)/", stripped):
            continue
        if stripped.startswith("Evidence note:"):
            continue
        if stripped.startswith("- Reader status:"):
            continue
        if stripped.startswith("- Confidence:"):
            continue
        if stripped.startswith("Before final publication"):
            continue
        if stripped.startswith("Current clean-reader unit:"):
            continue
        if stripped.startswith("Review:"):
            continue
        if stripped.startswith("Synoptic controls:"):
            continue
        if stripped.startswith("Synoptic/control file:"):
            continue
        if "DCLP XML" in stripped:
            continue
        if "квадратні дужки позначають" in stripped:
            continue
        if "square brackets mark" in stripped:
            continue
        if "supplied lost text" in stripped:
            continue
        if stripped.startswith("Status:"):
            continue
        if "publication-facing draft" in stripped:
            continue
        if stripped.startswith("Synoptic/control files:"):
            continue
        if stripped.startswith("Cluster/context notes:"):
            continue
        if "Картка:" in stripped or stripped.startswith("Card:"):
            continue
        if stripped.startswith("- окремий synoptic/control file"):
            continue
        if stripped.startswith("- окремий cluster/context note"):
            continue
        line = line.rstrip()
        if lang == "uk":
            line = localize_paper_statuses_uk(line)
        if lang == "en":
            line = normalize_en_reader_caution_language(line)
        lines.append(line)
    return "\n".join(lines).strip()


def load_reconstructed_greek_by_logion() -> dict[str, str]:
    """Collect already-vetted Greek reconstructions for paper-facing appendix use."""
    greek_by_logion: dict[str, str] = {}
    if PARALLEL_EDITION.exists():
        for line in PARALLEL_EDITION.read_text(encoding="utf-8").splitlines():
            if not line.startswith("| Logion "):
                continue
            cells = [cell.strip() for cell in line.strip().strip("|").split("|")]
            if len(cells) < 3:
                continue
            unit_match = re.fullmatch(r"Logion\s+(\d+)", cells[0])
            if not unit_match:
                continue
            greek_match = re.search(r"Greek retroversion, hypothetical:\s*`([^`]+)`", cells[2])
            if greek_match:
                greek_by_logion[unit_match.group(1)] = greek_match.group(1).strip()

    for card_path in sorted(CARDS_DIR.glob("logion-*.md")):
        number_match = re.search(r"logion-(\d+)\.md$", card_path.name)
        if not number_match:
            continue
        logion = str(int(number_match.group(1)))
        text = card_path.read_text(encoding="utf-8")
        match = re.search(
            r"Greek retroversion from Coptic/control layer:\s*\n\s*>\s*([^\n]+)",
            text,
        )
        if match:
            greek_by_logion.setdefault(logion, match.group(1).strip())
    return greek_by_logion


def normalize_uk_appendix_greek_blocks(text: str) -> str:
    """Replace reader-hostile 'not preserved' Greek blocks with reconstruction wording."""
    greek_by_logion = load_reconstructed_greek_by_logion()
    lines = text.splitlines()
    out: list[str] = []
    index = 0
    current_logion: str | None = None
    negative_markers = (
        "не збереж",
        "not extant",
        "no extant greek",
        "завантажен",
        "будь-яка грецька форма",
        "немає extant greek",
        "немає прямого збереженого грецького",
    )
    while index < len(lines):
        line = lines[index]
        logion_match = re.match(r"^## Логія\s+(\d+)\b", line)
        if logion_match:
            current_logion = logion_match.group(1)

        if line.strip() in {"### Грецький текст", "### Грецький свідок"}:
            heading = "### Грецький текст"
            section: list[str] = [line]
            index += 1
            while index < len(lines) and not lines[index].startswith("### ") and not lines[index].startswith("## "):
                section.append(lines[index])
                index += 1
            section_text = "\n".join(section).lower()
            has_negative_status = any(marker in section_text for marker in negative_markers)
            greek = greek_by_logion.get(current_logion or "")
            if has_negative_status:
                if greek:
                    out.append(heading)
                    out.append("")
                    out.append("Реконструйований грецький текст:")
                    out.append("")
                    out.append(f"> {greek}")
                else:
                    pass
                continue
            if line != heading:
                section[0] = heading
            out.extend(section)
            continue

        out.append(line)
        index += 1
    return "\n".join(out)


